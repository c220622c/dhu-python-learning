def issue4() -> None:
    x = input("输入代表星期的数字: ")
    if x == "1":
        print("星期一")
    elif x == "2":
        print("星期二")
    elif x == "3":
        print("星期三")
    elif x == "4":
        print("星期四")
    elif x == "5":
        print("星期五")
    elif x == "6":
        print("星期六")
    elif x == "7":
        print("星期日")
    else:
        print("输入错误，请输入1到7之间的数字。")


issue4()
